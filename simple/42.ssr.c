#include<stdio.h>
int main()
{
    int n,max,min;
    scanf("%d",&n);
    int a[n];
    for(int i=0;i<=n-1;i++)
    {
        scanf("%d",&a[i]);
        if(i==0)
        {
            max=a[i];min=a[i];
        }//值得注意
        
            if (max<a[i])
        {
            max=a[i];
        }
        if(min>a[i])
        {
            min=a[i];
        }
        
        
    }
    printf("%d %d",max,min);
    return 0;
}
// 最大最小值【C/C++/Java/Python】
// 描述
// 给定n个整数，求出其中的最大值和最小值

// 比如给定9个整数： 12 45 62 35 74 11 96 32，最大值是96，最小值是11

// 再比如给定10个整数：14 52 63 69 58 47 10 5 99 88，最大值是99，最小值是5

// 再比如给定12个整数：15 25 84 74 96 2 30 14 25 18 55 33，最大值是96，最小值是2

// 格式
// 输入格式
// 两行。第一行是一个正整数n（2≤n≤10,000），表示后续的整数个数。
// 第二行是n个正整数，中间以空格分隔。

// 输出格式
// 两个整数值，第一个是最大值，第二个是最小值，中间以空格分隔

// 样例
// 输入样例
// 15
// 12  32  69  84  71  20  35  31  25  29  68  36  48  47  9
// 输出样例
// 84 9