#include<stdio.h>
int main()
{
    int x,n;
    scanf("%d%d",&x,&n);
    double sum=x;
    for(int i=1;i<=n;i++)
    {
        
        //x//x+x*0.01//x+x*0.01+x*0.01

        //x=x+x*0.001,不能用x直接算，故用sum代替

        sum=sum+sum*0.001;

    }
    printf("%.4lf",sum);
    return 0;
}
// 人口增长
// 描述
// 我国现有x亿人口，按照每年0.1%的增长速度，n年后将有多少人？计算结果保留小数点后四位输出。

// 格式
// 输入格式
// 一行，包含两个整数x和n，分别是人口基数和年数，以单个空格分隔。

// 输出格式
// 输出最后的人口数，以亿为单位，保留到小数点后四位。1≤x≤100,1≤n≤100。

// 样例
// 输入样例
// 13 10
// 输出样例
// 13.1306