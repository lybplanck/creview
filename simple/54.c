#include<stdio.h>
int main()
{
    int n,s;
    scanf("%d",&n);
    if(n<=100)
    {
        s=400;
    }
    if(n>100&&n<=500)
    {
        s=400+(n-100)*4;
    }
    if(n>500&&n<=2000)
    {
        s=400+400*4+(n-500)*3;
    }
    if(n>2000)
    {
        s=400+400*4+1500*3+(n-2000)*2;
    }
    printf("%d",s);
    return 0;
}
